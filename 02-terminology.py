# Expression 
# = Value + Operator -> Will be evaluated down to a single value
2 + 2
(5-1)*((7+1)/(3-1))

# Data types
42              # Number
3.14            # Float
'Hello world!'  # String

'Alice' + 'Bob'
'Alice' * 3

# Variables
spam = 42   # Value assignment
spam        # Variable is evaluated to its value
spam = 'Hello'
spam + ' world'

# Statement
# = aka. instruction or command -> Will NOT be evaluated to a single value and can have one or more expression
spam = 2 + 2
